package exercises;

public class Javaexercise10 {

	public static void main(String[] args) {
		
		int a=4; 
		 
		//Switch expression 
		 switch(a){ 
		 
		 
		 //Case statements 
		 case 1: System.out.println("I am @ Home"); 
		 break; 
		 
		 case 2: System.out.println("I am @ Office"); 
		 break; 
		 
		 case 3: System.out.println("I am @ school"); 
		 break; 
		 
		 case 4: System.out.println("I am @ Outing"); 
		 break; 
		 //Default case statement 
		 default:System.out.println("Not @ Home, Office, Outing, or school");
		 
		 }
		
		
	}

}
